a = "Hello, World! now "

print(a[1]) # displays the second char

print(a[2:5]) #displays all char between 3rd and 4th char

print(a.strip()) # removes white spaces

print(len(a)) #this displays the char length / number of letter

print(a.lower()) # as name implies

print(a.upper())# as name implies

print(a.replace("H", "J")) # this replaces the H with J 

print(a.split(",")) # splits from ','